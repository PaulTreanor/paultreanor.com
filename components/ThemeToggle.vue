<template>
  <label class="switch">
    <input v-model="toggle" type="checkbox">
    <span class="slider round" />
  </label>
</template>

<script>
export default {
  name: 'ThemeToggle',
  data () {
    return {
      toggleValue: false
    }
  },
  computed: {
    toggle: {
      get () {
        return this.toggleValue
      },
      set (val) {
        this.$emit('darkMode', val)
      }
    }
  }
}
</script>

